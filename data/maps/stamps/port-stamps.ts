export function getOrientedStamp(
  id: string,
  side: Side
): {
  assets: {
    localAssets: AuthoringAsset[];
    footprint?: {
      width: number;
      height: number;
    };
  };
  canonical: PortStamp;
} {
  const canonical = getPortStamp(id);
  if (!canonical.oriented) {
    throw new Error(`Stamp '${id}' is missing oriented variants`);
  }
  const oriented = canonical.oriented[side];
  if (!oriented) {
    throw new Error(`Stamp '${id}' has no oriented variant for side '${side}'`);
  }
  return { assets: oriented, canonical };
}
